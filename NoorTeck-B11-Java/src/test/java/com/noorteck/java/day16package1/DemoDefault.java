 class DemoDefault {
	
	// Static variable with default access modifier'
	 static double price=10.2;
	 
	// Instance variable with default access modifier'
	 String country="USA";
	 
	// Static Method with default access modifier'
	 static void add (int a , int b) {
	 System.out.println(a+b);}
	//Non- Static Method with default access modifier'
	 void subtract(int a , int b) {
		 System.out.println(a-b);
	 }

}
