package com.noorteck.java.day8;

public class DemoWhile2 {
	public static void main (String args[]) {
		
		
		//Task 1 Using for loop;
		//loop through numbers from 20 up to 50 and print each number inside the loop body
		int i = 20;
		while (i <= 50) {
			System.out.println(i);
			i++;
		}
		System.out.println ("******************************");
		
		//Task 2 Using for loop;
		//loop through from 40 to 30 and print each number inside the loop body
		
		int x = 40;
		while ( x >= 30) {
			System.out.println (x);
			x--;
		}
		
		
		
	}
	}


