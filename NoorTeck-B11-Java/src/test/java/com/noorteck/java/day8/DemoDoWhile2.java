package com.noorteck.java.day8;

public class DemoDoWhile2 {
	public static void main (String args []) {
		
	// For loop
		for (int i = 5; i<= 0; i++) {
			System.out.println( i);
		}
		System.out.println("*******");
		
		
		// While loop
		int j = 5;
		while (j<=0) {
			System.out.println(j);
			j++;
			
		}
		
		System.out.println("*******");
		//Do While
		int y = 5;
		do {
			System.out.println (y);}
		while (y <= 0);
		System.out.println ("**************");
		
		
	}

}
