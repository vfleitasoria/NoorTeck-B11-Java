package com.noorteck.java.day8;

public class Average {
	public static void main (String args[]) {
		
		int total=0;
		
		for(int i=1; i<=10; i++){
			total = total + i;
			
			}System.out.println("Average: "+ total/10);
			
	System.out.println("*************************");
	
	
	
	int x=1;
	int toTal=0;
	while (x<=10) {
		toTal=toTal+x;
		x++;
		System.out.println("Average: "+toTal/10);
	}
	}
	

}
