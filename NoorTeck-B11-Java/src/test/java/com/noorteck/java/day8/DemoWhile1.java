package com.noorteck.java.day8;

public class DemoWhile1 {
	public static void main(String args []){
		
		// loop 1 - 5 using while loop
		// control variable
		
		
		int i= 1;
		do {
			System.out.println (i);
			i++;
		}while (i <= 5);
		System.out.println ("*****************");
		
		//Loop number 1 - 5 ---> calculate the sum of numbers between 1 - 5
		
		int total = 0;
		for (int x = 1; x <= 5; x++) {
			total = total + x;
		}
		System.out.println(total);
		
		
		}
		
		
			
		
		
	}


