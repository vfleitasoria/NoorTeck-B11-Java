package com.noorteck.java.day6;

public class DemoSwitch2 {
	public static void main(String args[]) {

		int favNum = 7;
		switch (favNum) {
		case 3:
			System.out.println("Ahmed's fav num....");
			break;
		case 5:
			System.out.println("Cindy's fav num...");
			break;
		case 7:
			System.out.println("Alishan's fav num...");
			break;
		case 26:
			System.out.println("Jackie's fav num...");
			break;
		default:
			System.out.println("Nobody's fav num...");

		}

	}

}
