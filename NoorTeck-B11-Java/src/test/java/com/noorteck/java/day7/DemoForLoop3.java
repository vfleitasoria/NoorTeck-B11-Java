package com.noorteck.java.day7;

public class DemoForLoop3 {
	public static void main(String args[]) {

		for (int i = 1; i <= 31; ++i) {

			if (i == 9) {
				System.out.println("9 is Zain Bday");
			} else if (i == 16) {
				System.out.println("16 is Faraz Bday");
			} else if (i == 10) {
				System.out.println("10 is Kamy's Bday");
			} else if (i == 26) {
				System.out.println("Jackie's Bday");
			}else {
				System.out.println(i);
			}
		}

	}}

