package com.noorteck.java.day7;

public class DemoForLoop2 {
	public static void main (String args []) {
		
		/** Task 1. Using for loop,
		* loop through number from 20 up 50 and print each number inside the loop body
		 */
		for (int i = 20;i<= 50; i++) {
			System.out.println (i);}
			System.out.println ("*************************");
			/** Task 2. Using for loop,
			 * loop through numbers from 40 to 30 and print inside loop body
			 */
			
			for (int i = 40; i>=30; i--) {
				System.out.println (i);}
				
			}
		
		
		
		
	}


