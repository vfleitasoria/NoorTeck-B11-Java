package com.noorteck.java.day3;

public class Calculator {
	public static void main(String args[]) {

		int n1 = 20;
		int n2 = 30;
		int result1 = n1 + n2;

		System.out.println(n1 + " + " + n2 + " = " + result1);

		System.out.println("********************");

		// Print the following format ----> 20 + 30 = 50

		System.out.println();
		double a1 = 55.0;
		double a2 = 5.0;
		double result2 = a1 - a2;
		// Print in a format 55.0 - 5.0 = 50.0

		System.out.println(a1 + " - " + a2 + " = " + result2);

	}

}
