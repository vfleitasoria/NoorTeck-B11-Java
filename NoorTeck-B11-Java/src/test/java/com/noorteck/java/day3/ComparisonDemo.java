package com.noorteck.java.day3;

public class ComparisonDemo {

	public static void main(String args[]) {

		int n1 = 10;
		int n2 = 20;

		System.out.println(n1 == n2);
		System.out.println(n1 != n2);
		System.out.println(n1 > n2);
		System.out.println(n1 < n2);
		System.out.println(n1 >= n2);
		System.out.println(n1 <= n2);

	}

}

/**
 * Comparison Operator is use to compare two values Operator Description
 * ------------------------------- == Equal to != Not Equal > Greater than <
 * Less than >= Greater than or Equal <= Less than or Equal
 */
