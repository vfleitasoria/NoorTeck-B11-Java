package com.noorteck.java.day33;
//interface
public interface Department {
	
	int a = 10;                       //public static final
	public int b = 11;                //public static final
	public static int c = 12;         //public static final
	public final static int  d = 13;  //public static final
	static int e = 14;                //public static final
	final static int f = 15;          //public static final

}
/**
In interface 
by default all the variables are public static final variables
In interface we cannot have an instance variable/static variable
we can only have public static final variables




*/